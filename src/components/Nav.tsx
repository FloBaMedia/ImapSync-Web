export function Nav() {
  const pathname = usePathname()
  const router = useRouter()

  const handleLogout = async () => {
    await fetch('/api/auth/logout', { method: 'POST' })
    router.push('/login')
    router.refresh()
  }

  return (
    <nav className="w-60 bg-[#0a0a12] border-r border-[#1e1e2e] min-h-screen flex flex-col shrink-0">
      <div className="p-5 border-b border-[#1e1e2e]">
        <div className="flex items-center gap-3">
          <div className="w-8 h-8 bg-blue-600 rounded-lg flex items-center justify-center text-white font-bold text-sm">IS</div>
          <div>
            <h1 className="text-sm font-semibold text-white">ImapSync Web</h1>
            <p className="text-xs text-gray-500">Email Migration</p>
          </div>
        </div>
      </div>

      <div className="flex-1 p-3 space-y-0.5">
        {navItems.map(item => {
          const isActive = item.href === '/' ? pathname === '/' : pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all ${
                isActive
                  ? 'bg-blue-600/20 text-blue-400 border border-blue-600/20'
                  : 'text-gray-400 hover:text-gray-100 hover:bg-[#1a1a2e]'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}
      </div>

      <div className="p-3 border-t border-[#1e1e2e]">
        <button
          onClick={handleLogout}
          className="w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm text-gray-500 hover:text-gray-300 hover:bg-[#1a1a2e] transition-all"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Sign out
        </button>
      </div>
    </nav>
  )
}
